#include<bits/stdc++.h>
using namespace std;

auto sum( auto x, auto y)
{

    return x+y;
}
int main()
{

    cout<<sum(4,5)<<endl;
    cout<<sum(4.5,6.7)<<endl;
    cout<<sum(2,1.2)<<endl;
    cout<<sum(1.3,8)<<endl;
    cout<<sum('A','a')<<endl;
    cout<<sum('A',45)<<endl;
    cout<<sum(4.25,'E')<<endl;
    return 0;

}
